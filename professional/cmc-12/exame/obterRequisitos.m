function req = obterRequisitos()
    req.wn_theta = 3*2*pi;
    req.xi_theta = sqrt(2)/2;
    
    req.wn_phi = 3*2*pi;
    req.xi_phi = sqrt(2)/2;
    
    req.wn_psi = 3*2*pi;
    req.xi_psi = sqrt(2)/2;
    
    req.wn_x = 0.5*2*pi;
    req.xi_x = sqrt(2)/2;
    
    req.wn_y = 0.5*2*pi;
    req.xi_y = sqrt(2)/2;
    
    req.wn_z = 0.5*2*pi;
    req.xi_z = sqrt(2)/2;

end